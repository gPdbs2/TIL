# abs : 절댓값 구하기
print(abs(-5))      # 5
# pow(a, b) : a의 b승
print(pow(4, 2))    # 4^2 = 4*4 = 16
# max : 입력 받은 값 중 최댓값
# min : 입력 받은 값 중 최솟값
print(max(5, 12))   # 12
print(min(5, 12))   # 5
# round : 반올림
print(round(3.14))  # 3.1
print(round(4.99))  # 5

# math 라이브러리 활용
from math import *
# floor : 내림
print(floor(4.99))  # 4
# ceil : 올림
print(ceil(3.14))   # 4
# sqrt : 제곱근 구하기
print(sqrt(16))     # 4